/*
    Deteccion de tipo de datos en JS
    - Esto se hace con la funcion typeof() y enviando como parametro la variable en cuestion
    - Esta funcion puede usarse para comprobar datos antes de realizar operaciones.
*/

'use strict'

// Declaracion de variables - Para realizar pruebas
var a = "Cadena de texto";
var b = 1;
var c = 1.5;
var d = false;

